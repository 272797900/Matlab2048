function index = getIndex(num)
switch num
    case 0
        index = 1;
    case 2
        index = 2;
    case 4
        index = 3;
    case 8
        index = 4;
    case 16
        index = 5;
    case 32
        index = 6;
    case 64
        index = 7;
    case 128
        index = 8;
    case 256
        index = 9;
    case 512
        index = 10;
    case 1024
        index = 11;
    case 2048
        index = 12;
end
